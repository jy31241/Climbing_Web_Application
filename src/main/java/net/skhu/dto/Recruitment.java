package net.skhu.dto;

public class Recruitment {
	int id;
	int mountian_id;
	int boardType_id;
	int maxNum;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getMountian_id() {
		return mountian_id;
	}

	public void setMountian_id(int mountian_id) {
		this.mountian_id = mountian_id;
	}

	public int getBoardType_id() {
		return boardType_id;
	}

	public void setBoardType_id(int boardType_id) {
		this.boardType_id = boardType_id;
	}

	public int getMaxNum() {
		return maxNum;
	}

	public void setMaxNum(int maxNum) {
		this.maxNum = maxNum;
	}

}
